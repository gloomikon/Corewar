#include "libft.h"

int	ft_strchri(const char *s, int c)
{
	size_t	i;

	i = -1;
	while (s[++i])
	{
		if (s[i] == (char)c)
			return (i);
	}
	if (c == 0)
		return (i);
	return (-1);
}

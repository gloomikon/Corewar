#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char const	*start;
	char const	*end;
	char		*result;

	start = NULL;
	if (s == NULL)
		return (NULL);
	while (*s)
	{
		if (!(*s == ' ' || *s == '\n' || *s == '\t'))
		{
			start = (start == NULL) ? s : start;
			end = s;
		}
		s++;
	}
	if (start == NULL)
		return (ft_strnew(0));
	if ((result = (char *)malloc(sizeof(char) * (end - start + 2))) == NULL)
		return (NULL);
	s = start;
	while (s <= end)
		*result++ = *s++;
	*result = '\0';
	return (result - (end - start + 1));
}

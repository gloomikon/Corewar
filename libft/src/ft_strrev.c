#include "libft.h"

char	*ft_strrev(char *s)
{
	int i;
	int a;
	int b;

	i = ft_strlen(s);
	a = 0;
	b = i / 2 + i % 2;
	while (i > b)
	{
		*(s + a) = *(s + a) + *(s + i - 1);
		*(s + i - 1) = *(s + a) - *(s + i - 1);
		*(s + a) = *(s + a) - *(s + i - 1);
		a++;
		i--;
	}
	return (s);
}

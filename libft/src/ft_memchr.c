#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = (unsigned char*)s;
	i = -1;
	while (++i < n)
		if (ptr[i] == (unsigned char)c)
			return ((void*)(ptr + i));
	return (NULL);
}

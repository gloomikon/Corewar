#include "libft.h"
#include <unistd.h>

void	ft_putarr(char **arr)
{
	int j;

	j = 0;
	while (*(arr + j))
	{
		ft_putstr((*(arr + j)));
		j++;
		write(1, "\n", 1);
	}
}
